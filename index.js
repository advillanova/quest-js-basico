function subtracao(numero1, numero2) {
    return numero1 - numero2
}

let resultadoDaSubtracao = subtracao(2022, 1989)
console.log('A subtração dos dois números é: ' + resultadoDaSubtracao)

function soma(numero1, numero2) {
    return numero1 + numero2
}

let resultadoDaSoma = soma(5, 5)
console.log('A soma dos dois números é: ' + resultadoDaSoma)

function multiplicacao(numero1, numero2) {
    return numero1 * numero2
}

let resultadoDaMultiplicacao = multiplicacao(5, 3)
console.log('O resultado da multiplicação dos dois números é: ' + resultadoDaMultiplicacao)

function divisao(numero1, numero2) {
    return numero1 / numero2
}

let resultadoDaDivisao = divisao(1000000, 2)
console.log('O resultado da divisão dos dois números é: ' + resultadoDaDivisao)